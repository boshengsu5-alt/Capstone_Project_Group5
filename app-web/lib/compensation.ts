export type CompensationStatus =
  | 'under_review'
  | 'awaiting_signature'
  | 'awaiting_payment'
  | 'partially_paid'
  | 'paid'
  | 'waived';

export interface EstimateCompensationInput {
  purchasePrice: number | null;
  purchaseDate: string | null;
  severity: string;
  reportStatus?: string | null;
  autoGenerated?: boolean | null;
  reporterId?: string | null;
  borrowerId?: string | null;
}

export const DAMAGE_COEFFICIENT: Record<string, number> = {
  minor: 0.2,
  moderate: 0.5,
  severe: 1.0,
  lost: 1.0,
};

